def leedsbutterfly_generator_final(sess, batch_size, train=True):

    imgs_list = []
    labels_list = []
    
    if train:
        path_imgs = path_imgs_train_augmented
    else:
        path_imgs = path_imgs_test_augmented
    
    filenames = os.listdir(path_imgs)
    np.random.shuffle(filenames)
    
    for filename in filenames:
        if not filename.endswith("png"):
            continue
        label = int(filename[:3]) - 1
        labels_list += [label]
        img = cv2.imread(os.path.join(path_imgs, filename), 1)
        imgs_list += [img]
        
        if len(imgs_list) == batch_size:
            one_hot = sess.run(tf.one_hot(labels_list, 10))
            yield one_hot, imgs_list
            imgs_list = []
            labels_list = []


